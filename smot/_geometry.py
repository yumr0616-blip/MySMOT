"""Shared geometry primitives for box sequences.

Deterministic (not learned). Used by both motion_facts.py and event_filter.py
so the two "real" deterministic modules don't duplicate identical math.
"""
from __future__ import annotations

import math

from smot.types import Box


def centroid(box: Box) -> tuple[float, float]:
    x1, y1, x2, y2 = box
    return ((x1 + x2) / 2.0, (y1 + y2) / 2.0)


def dist(a: tuple[float, float], b: tuple[float, float]) -> float:
    return math.hypot(a[0] - b[0], a[1] - b[1])


def iou(a: Box, b: Box) -> float:
    ax1, ay1, ax2, ay2 = a
    bx1, by1, bx2, by2 = b
    ix1, iy1 = max(ax1, bx1), max(ay1, by1)
    ix2, iy2 = min(ax2, bx2), min(ay2, by2)
    iw, ih = max(0.0, ix2 - ix1), max(0.0, iy2 - iy1)
    inter = iw * ih
    area_a = max(0.0, ax2 - ax1) * max(0.0, ay2 - ay1)
    area_b = max(0.0, bx2 - bx1) * max(0.0, by2 - by1)
    union = area_a + area_b - inter
    if union <= 0.0:
        return 0.0
    return inter / union


def orientation(a: tuple[float, float], b: tuple[float, float]) -> float:
    """Angle in radians from point a to point b."""
    return math.atan2(b[1] - a[1], b[0] - a[0])
